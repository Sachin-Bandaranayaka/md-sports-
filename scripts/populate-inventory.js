// SKU generation helper function
function generateSKU(productName, category, index) {
  // Category prefixes
  const categoryPrefixes = {
    'BAGS': 'BAG',
    'CLOTHS': 'CLO', 
    'GRIPS': 'GRP',
    'OTHER': 'OTH',
    'NETS': 'NET',
    'RACKETS': 'RAC',
    'SHOES': 'SHO',
    'SHUTTLECOCKS': 'SHU',
    'SOCKS': 'SOC',
    'STRINGS': 'STR',
    'WRISTBANDS': 'WRI'
  };

  // Brand prefixes
  const brandPrefixes = {
    'YONEX': 'YON',
    'LI NING': 'LIN', 
    'HUNDRED': 'HUN',
    'MAXBOLT': 'MAX',
    'VICTOR': 'VIC',
    'FELET': 'FEL',
    'KONEX': 'KON',
    'OQO': 'OQO',
    'SPACESHIP': 'SPA',
    'MAVIS': 'MAV',
    'VONEX': 'VON',
    'LIN MEI': 'LME',
    'LI MEI': 'LME',
    'GOSEN': 'GOS',
    'APACS': 'APA',
    'DUNLOP': 'DUN',
    'IODEX': 'IOD'
  };

  const categoryPrefix = categoryPrefixes[category] || 'GEN';
  
  // Extract brand from product name
  let brandPrefix = 'GEN';
  for (const [brand, prefix] of Object.entries(brandPrefixes)) {
    if (productName.toUpperCase().includes(brand)) {
      brandPrefix = prefix;
      break;
    }
  }

  // Generate unique sequential number with timestamp to avoid conflicts
  const timestamp = Date.now().toString().slice(-4); // Last 4 digits of timestamp
  const sequentialNumber = String(index).padStart(4, '0');
  
  return `${categoryPrefix}-${brandPrefix}-${sequentialNumber}-${timestamp}`;
}

